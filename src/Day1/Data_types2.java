package Day1;

public class Data_types2 {

	public static void main(String[] args) {
		long list=5555555L;
		System.out.println(list);
		
		float list1=555.555f;
		System.out.println(list1);
		
		int a=100, b=200, c=300;
		
		System.out.println("this is the most priodic number:"+a);
		System.out.println("this is the most least number:"+a+" "+b+" "+c);
		
		char ch='A'; // for char use single quotes
		String ch1= "Vishnu"; // for string use double quotes
		
		System.out.println(ch);
		System.out.println(ch1);
		
		boolean vishnu=true;
		boolean vardhan=false;
		System.out.println(vishnu);
		System.out.println(vardhan);
				

	}

}
