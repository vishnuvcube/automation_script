package Day1;

public class assignment_operator {

	public static void main(String[] args) {
		//Assignment = += -= *= /= %=
		
		int a =10;
		
		a+=5;
		System.out.println(a);
		
		int b=15;
		b-=5;
		System.out.println(b);
		
		int c=20;
		c*=2;
		System.out.println(c);
		
		int d=25;
		d/=5;
		System.out.println(d);
		
		int e=30;
		e%=2;
		System.out.println(e);


	}

}
